
public class Pencils {

	public static void main(String[] args) {
		//Aliyah had $24 to spend on seven pencils. 
			int aliyahHad = 24;
		
		//After buying them she had $10. 
			int afterPurchase = 10;
		
		//How much did each pencil cost?
			int eachPencilCost = (aliyahHad - afterPurchase) / 7;
	
		System.out.println("Each pencil cost $" + eachPencilCost + ".");
	}

}
