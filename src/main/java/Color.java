
public class Color {

	public static void main(String[] args) {
		// if/else example with colors

		String color = "purple";
		{

			if (color.equals("red")) {
				System.out.println("I am red.");
			} else if (color.equals("aquamarine")) {
				System.out.println("It's a little blue and a little green");
			} else if (color.equals("purple")) {
				System.out.println("Purple is the best color!");
			} else {
				System.out.println("This is a great color too!");
			}

		}

	}
}