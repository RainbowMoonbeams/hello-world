
public class LisaMuffins {

	public static void main(String[] args) {
		//The recipe calls for 7 cups of sugar.
		int requiredCups = 7;
		
//		She has already put in 2 cups.
		int addedCups = 2;
				
//		How many more cups does she need?
		int neededCups = requiredCups - addedCups;
		
		System.out.println("She needs " + neededCups + " more cups");
		

	}

}
