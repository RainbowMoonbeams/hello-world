
public class MakingMoney {

	public static void main(String[] args) {
		// Trevon had $29
		int heHad = 29;
				
//		Trevon now has $41
		int heNowHas = 41;
		
//	How much did Trevon make for cleaning the attic?
		int heMade = heNowHas - heHad;
		
		System.out.println("Trevon made $" + heMade + " for cleaning the attic.");

	}

}
